package com.bridgelabz;

public class SwapNumbers {
public static void main(String[] args) {
	int a=5;
	int b=8;
	System.out.println("the value  of a is  "+a+ " the value of b is "+b);
	System.out.println("After Swapping");
	int temp=0;
	temp=a;
	a=b;
	b=temp;
	System.out.println("the value  of a is  "+a+ " the value of b is "+b);

	
}
}
